//Math é um objeto especial do Javascript que nos ajuda a trabalhar com numeros

//Recebendo o valor de PI
let pi = Math.PI;
console.log(`Valor de Pi: ${pi}`);

//Recebendo o número de Euler
let e = Math.E;
console.log(`Número de euler: ${e}`);

//Arredondando um número
let var1 = Math.round(4.6);
console.log(`4.6 arredondado = ${var1}`);

//Arredondando um número para cima
let var2 = Math.ceil(4.9);
console.log(`4.9 arredondado para cima = ${var2}`);

//Arredondando para baixo
let var3 = Math.floor(4.9);
console.log(`4.9 arredondado para baixo = ${var3}`);

//Ignorando a parte decimal
let var4 = Math.trunc(4.9);
console.log(`4.9 sem a casa decimal = ${var4}`);