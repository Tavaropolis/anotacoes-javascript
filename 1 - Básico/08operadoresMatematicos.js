//Os operadores existentes em Javascript são:
    //Adição: +
    //Subtração: -
    //Divisão: /
    //Multiplicação: *
    //Resto: %
    //Exponenciação: **

console.log(`4 + 2 = ${4 + 2}`);
console.log(`4 - 2 = ${4 - 2}`);
console.log(`4 * 2 = ${4 * 2}`);
console.log(`4 / 2 = ${4 / 2}`);
console.log(`4 % 2 = ${4 % 2}`);
console.log(`4 ** 2 = ${4 ** 2}`);