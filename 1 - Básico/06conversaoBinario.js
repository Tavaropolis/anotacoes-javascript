//É possível converter números para binário com a função toString()
let var1 = 10;
var1 = var1.toString(2);
console.log(`10 em binário: ${var1}`);

//Para octal
let var2 = 10;
var2 = var2.toString(8);
console.log(`10 em octal: ${var2}`)

//Para Hexadecimal
let var3 = 15;
var3 = var3.toString(16);
console.log(`15 em hexadecimal: ${var3}`)